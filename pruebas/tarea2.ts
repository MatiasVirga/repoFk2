function areaOfASquare(sideFirst: number, sideSecond :number): number{
    return sideFirst * sideSecond;
}
console.log(areaOfASquare(2,2));


const pi: number = 3.14;
function areaOfACircle(radio:number) :number{
    return (pi * radio) * radio;
}
console.log(areaOfACircle(5));

function areaOfATriangle(base:number, height:number) :number{
    return (base * height) / 2;
}
console.log(areaOfATriangle(5,2))

